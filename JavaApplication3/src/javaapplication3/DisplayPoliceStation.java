    private void getNewData()
    {
        try {
            rs = db.executeQuery("SELECT stationID, address, headID, state FROM police_station ORDER BY stationID ASC ");
            rs.beforeFirst();
            rs.first();
            populateFields();
            
        } catch (SQLException ex) {
             Logger.getLogger(DisplayCriminal.class.getName()).log(Level.SEVERE, null, ex);      
        }
    }
    private void populateFields() {
        try {
            StationIDText.setText(rs.getString("StationID"));
            AddressText.setText(rs.getString("address"));
            HeadIDText.setText(rs.getString("headID"));
            StateText.setText(rs.getString("state"));
            EnableDisableButtons();

            
        } catch (SQLException ex) {
            Logger.getLogger(EditPoliceOfficer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void EnableDisableButtons()
    {
        try {
            if (rs.isFirst()) {
                PrevButton.setVisible(false);
            } else {
                PrevButton.setVisible(true);
            }
            if (rs.isLast()) {
                NextButton.setVisible(false);
            } else {
                NextButton.setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DisplayCriminal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    private void PrevButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrevButtonActionPerformed
        try {
            if (!rs.isFirst()) {
                rs.previous();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(DisplayCriminal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_PrevButtonActionPerformed

    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        try {
            if (!rs.isLast()) {

                rs.next();
                populateFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(DisplayCriminal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_NextButtonActionPerformed


