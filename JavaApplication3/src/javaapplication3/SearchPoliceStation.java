    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if(jTextField6.getText().isEmpty() || jTextField6.getText().length()>4 || !Validation.isInteger(jTextField6.getText().trim()) || Integer.valueOf(jTextField6.getText())<0)
        {
            if(jTextField6.getText().isEmpty()) JOptionPane.showMessageDialog(null, "Please Type A number");
            else if(jTextField6.getText().length()>4) JOptionPane.showMessageDialog(null, "Station ID must be 4 digits");
            else if(!Validation.isInteger(jTextField6.getText().trim())) JOptionPane.showMessageDialog(null, "please type an integer");
            else JOptionPane.showMessageDialog(null, "Station ID cannot be negative");
        }
        else
        {
            String SQL = "SELECT police_station.stationid as IDSTATION, address, headid, state, fname, lname, hiredate, rank FROM police_station inner join officer on headid = officerid WHERE police_station.stationid = '" + jTextField6.getText().trim() + "'" + " ORDER BY police_station.stationID ASC";
            System.out.println(SQL);
            int size = 0;
            try {
                ResultSet rs = db.executeQuery(SQL);
                
                while (rs.next()) {
                    size++;
                }
            } catch (SQLException ex) {
                Logger.getLogger(SearchCriminal.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(size == 0)
            {
                JOptionPane.showMessageDialog(null, "Result set is empty");
            }
            else
            {
                new DisplayPoliceStation(db, SQL).setVisible(true);
            }
            
            
        }
    }//GEN-LAST:event_jButton6ActionPerformed

